package com.BabyTracker.Model;

public class DailyChildModel {

	String childActivityDate;
	int childMilk_quantity;
	int childDiper_status_change;
	
	public String getChildActivityDate() {
		return childActivityDate;
	}
	public void setChildActivityDate(String childActivityDate) {
		this.childActivityDate = childActivityDate;
	}
	public int getChildMilk_quantity() {
		return childMilk_quantity;
	}
	public void setChildMilk_quantity(int childMilk_quantity) {
		this.childMilk_quantity = childMilk_quantity;
	}
	public int getChildDiper_status_change() {
		return childDiper_status_change;
	}
	public void setChildDiper_status_change(int childDiper_status_change) {
		this.childDiper_status_change = childDiper_status_change;
	}
	
	
}
